package Jour1.Job09;

public class MyString {
    public static void main(String[] args) {
        // Déclaration et initialisation de variables de type String de différentes façons
        String str1 = "Première méthode"; // Méthode 1: Littéral de chaîne
        String str2 = new String("Deuxième méthode"); // Méthode 2: Instanciation d'un objet String
        String str3 = ""; // Méthode 3: Chaîne vide
        String str4 = null; // Méthode 4: Référence null
        
        // Affichage des valeurs des variables
        System.out.println("str1 : " + str1);
        System.out.println("str2 : " + str2);
        System.out.println("str3 : " + str3);
        System.out.println("str4 : " + str4);
    }
    
}
