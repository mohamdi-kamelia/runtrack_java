package Jour1.Job05;

public class Calcul {
    public static void main(String[] args) {
        // Déclaration et initialisation de la variable nombre
        int nombre = 7;

        // Afficher la valeur initiale de nombre
        System.out.println( nombre);

        // Ajouter 3 à la variable nombre et afficher la nouvelle valeur
        nombre += 3;
        System.out.println( nombre);

        // Multiplier la valeur de nombre par 5 et afficher la nouvelle valeur
        nombre *= 5;
        System.out.println( nombre);
    }
}
